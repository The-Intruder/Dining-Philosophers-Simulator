void	ft_perror(int type, char *cause)
{
	if (type == 1)
		write(2, RED"\033[4m\nError\033[0m\t", 20);
	else if (type == 2)
		write(2, YEL"\nWarning"NNN, 17);
	write(2, cause, ft_strlen(cause));
	write(2, "\n\n", 2);
}

